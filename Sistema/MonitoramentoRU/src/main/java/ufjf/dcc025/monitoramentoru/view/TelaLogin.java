    public void validar() {

        boolean sucesso;
        try {
            UsuarioController usuarioContoller = new UsuarioController();
            UsuarioLogin usuarioLogin = new UsuarioLogin(txtUser.getText(), txtPassword.getText());
            sucesso = usuarioContoller.autenticaLogin(usuarioLogin);

            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Login Realizado.");
                TelaPrincipal telaPrincipal = new TelaPrincipal();
                telaPrincipal.setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(this, "Os campos n√£o foram preenchidos corretamente.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex);
        }

    }

